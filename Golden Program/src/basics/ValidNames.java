package basics;

public class ValidNames {
    public static void main(String[] args) {
	
    //valid names or qualified identifiers
    	
    //A name cannot contain special character other than _ and $
    	
     int _n=10;
     int $n=20;
  // int #n=30;  //it rejects
     int _n$=40;
     
     //Apart from _ and $, a valid identifier cannot use any other special character
     //you cannot use space in between the characters
     
     //Remember always the variable name should start with lower case
     //method name should start with lower case
     //class name should start with upper case
     //package name should start with lower case
     
     //The above are not compulsion,but good programming standards as per OOPS.
          
   }
}
